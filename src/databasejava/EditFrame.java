    public void edit(String id) {
            DatabaseJava koneksi = new DatabaseJava();
        
           koneksi.koneksi();
        

        try {
            String sql = "select * from dosen where kode_dosen like '%" + id + "%'";

            PreparedStatement stmt = koneksi.con.prepareStatement(sql);
            ResultSet res = (ResultSet) stmt.executeQuery();

            while (res.next()) {
                
                 

                
                jTextField12.setText(res.getString("kode_dosen"));
                jTextField13.setText(res.getString("nama_dosen"));
                jTextField14.setText(res.getString("jenis_kelamin"));
                jTextField15.setText(res.getString("agama"));
                jTextField16.setText(res.getString("alamat"));

            }
            
            
//            dispose();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    public void update() {

        try {

            String url = "jdbc:mysql://localhost/JDBC";
            String username = "root";
            String password = "";
            Class.forName("com.mysql.jdbc.Driver");
            Connection koneksi = (Connection) DriverManager.getConnection(url, username, password);

            
          String query = "UPDATE dosen SET nama_dosen = ?, jenis_kelamin = ?,  agama = ?, alamat= ?  WHERE kode_dosen = ?;";
            PreparedStatement stmt = koneksi.prepareStatement(query);

          
            stmt.setString(1, jTextField13.getText());
            stmt.setString(2, jTextField14.getText());
            stmt.setString(3, jTextField15.getText());
            stmt.setString(4, jTextField16.getText());
            stmt.setString(5, jTextField12.getText());
            stmt.executeUpdate();

            stmt.close();

            JOptionPane.showMessageDialog(null, "Data berhasil diedit", "Informasi", JOptionPane.INFORMATION_MESSAGE);

//            new ProductsFrame().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal diedit", "Error", JOptionPane.ERROR_MESSAGE);
            System.out.println("Tidak dapat menemukan data !" + e.getMessage());
        }
    }
